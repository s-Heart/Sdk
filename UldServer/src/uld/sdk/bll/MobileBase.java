package uld.sdk.bll;

import java.io.Serializable;
import java.util.HashMap;

public class MobileBase implements Serializable {

	private String feeType = "";
	private String cpId = "";
	private String cPServiceID = "";
	private HashMap<String, Object> consumeCodeString = new HashMap<String, Object>();
	private String fID = "";
	private String packageID = "";
	private String cPSign = "";
	private String cPParam = "";

	public HashMap<String, Object> getConsumeCodeString() {
		return consumeCodeString;
	}

	public void setConsumeCodeString(HashMap<String, Object> consumeCodeString) {
		this.consumeCodeString = consumeCodeString;
	}

	public String getFeeType() {
		return feeType;
	}

	public void setFeeType(String feeType) {
		this.feeType = feeType;
	}

	public String getCpId() {
		return cpId;
	}

	public void setCpId(String cpId) {
		this.cpId = cpId;
	}

	public String getcPServiceID() {
		return cPServiceID;
	}

	public void setcPServiceID(String cPServiceID) {
		this.cPServiceID = cPServiceID;
	}

	public String getfID() {
		return fID;
	}

	public void setfID(String fID) {
		this.fID = fID;
	}

	public String getPackageID() {
		return packageID;
	}

	public void setPackageID(String packageID) {
		this.packageID = packageID;
	}

	public String getcPSign() {
		return cPSign;
	}

	public void setcPSign(String cPSign) {
		this.cPSign = cPSign;
	}

	public String getcPParam() {
		return cPParam;
	}

	public void setcPParam(String cPParam) {
		this.cPParam = cPParam;
	}
}
